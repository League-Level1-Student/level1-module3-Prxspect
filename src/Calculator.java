
public class Calculator {

	public int add(int num1, int num2, int ans) {
		ans = num1 + num2;
		return ans;
	}
	
	public int multiply(int num1, int num2, int ans) {
		ans = num1 * num2;
		return ans;
	}
	
	public int subtract(int num1, int num2, int ans) {
		ans = num1 - num2;
		return ans;
	}
	
	public int divide(int num1, int num2, int ans) {
		ans = num1 / num2;
		return ans;
	}
}

